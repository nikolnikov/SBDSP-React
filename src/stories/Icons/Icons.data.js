function IconMap({ color, isIllustrative, isLegacy, size }) {
    const iconData = icons[0];

    let selectedIcons = [];

    if (isIllustrative && iconData.illustrative) {
        selectedIcons = iconData.illustrative;
    } else if (isLegacy && iconData.legacy) {
        selectedIcons = iconData.legacy;
    } else {
        selectedIcons = iconData.flat;
    }

    return (
        <div className="icon-map">
            {selectedIcons.map((icon, index) => (
                <div key={index} className="icon-map__item">
                    <Icon
                        color={color}
                        name={icon.name}
                        isIllustrative={isIllustrative}
                        isLegacy={isLegacy}
                        size={size}
                    />
                    <span className="icon-map__item-name">{icon.name}</span>
                </div>
            ))}
        </div>
    );
}
